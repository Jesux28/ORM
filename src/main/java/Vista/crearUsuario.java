    private void BotonCrearActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BotonCrearActionPerformed
        //Creamos el Usuario
        String nombre = FieldNombre.getText();
        String pass = FieldPass.getText();
        String crearU = new ControladorUsuario().createUsuario(nombre, pass);
        //Creamos el Rol-User
        Transaction tx = sesion.beginTransaction();
        Query query = sesion.createQuery("from Usuario where nombre_Usuario= :nombre and pass= :pass");
        query.setParameter("nombre", nombre);
        query.setParameter("pass", pass);
        tx.rollback();
        List<Usuario> user = query.list();
        if(!user.isEmpty()){
            int rol = valores.get(ComboRoles.getSelectedIndex()-1);
            int cod = user.get(0).getId();
            String crearRol = new ControladorRolUser().createRolUser(rol, cod);
        }else{
            System.out.println("Error al dar rol");
        }
    }//GEN-LAST:event_BotonCrearActionPerformed

